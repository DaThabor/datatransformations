# Data Principles

![](images/Data principles.png){width=40%}


Isn't there a day going by without you searching something on Google? 

This book wit data principles will help you find your information quickly. "Quicker than Google", you might ask? Well, not entirely, but as everything you need in your day-to-day life is combined into this book in an easy read, you might actually be faster than finding things on Google...


'Data Principals' is a book for all data enthusiasts out there. In our daily lives we are working with data in many forms and ways, and if you are a Data Scientist, Data Engineer, BI consultant, Data Analyst, or any other profession working with data, you will always face some kind of problems when dealing with data.

This book will help you identify the basic principles of data in many ways. Each chapter deals with a particular part of data and explains the basic principles. With those principles and some guidance here and there, you should be able to find a solution for your particular problem and maybe learn something new along the way!